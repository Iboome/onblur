package com.example.model;

import javax.persistence.*;

@Entity
@Table(name="blog_tag")
public class Tag {
    private Integer id;
    private String  tag;
    private String  url;

    @Id
    @GeneratedValue
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
